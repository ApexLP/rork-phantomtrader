export const getTopPerformers = (limit: number = 10): Politician[] => {
  return [...MOCK_POLITICIANS]
    .sort((a, b) => b.avgReturn - a.avgReturn)
    .slice(0, limit);
};

export const getMostActivePoliticians = (limit: number = 10): Politician[] => {
  return [...MOCK_POLITICIANS]
    .sort((a, b) => b.totalTrades - a.totalTrades)
    .slice(0, limit);
};

export const getPoliticianById = (id: string): Politician | undefined => {
  return MOCK_POLITICIANS.find(p => p.id === id);
};

export const searchPoliticians = (query: string): Politician[] => {
  const lowerQuery = query.toLowerCase();
  return MOCK_POLITICIANS.filter(p => 
    p.name.toLowerCase().includes(lowerQuery) ||
    p.state.toLowerCase().includes(lowerQuery) ||
    p.party.toLowerCase().includes(lowerQuery) ||
    p.chamber.toLowerCase().includes(lowerQuery)
  );
};
